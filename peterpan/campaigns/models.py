class Campaign:
  def __init__(self, campaignId = 0, campaignTitle='', campaignLocation='', campaignDesc='', campaignDate='', startTime = '', endTime = ''):
    self.campaignId = campaignId
    self.campaignTitle = campaignTitle
    self.campaignLocation = campaignLocation
    self.campaignDesc = campaignDesc
    self.campaignDate = campaignDate
    self.startTime = startTime
    self.endTime = endTime
